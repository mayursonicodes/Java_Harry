public class J_001_data_types {
    public static void main(String[] args) {
        int a = 15;
        float b = 5.2f;

        double c = 1.2;
        double d = 1.2d; //! We can also write 1.2D

        long e = 42584258554155L; //! We can also write 999999999999l

        char f = 'M';

        boolean g = true;

        String str = "Mayur Soni"; //! Always use 'S' not 's'

        System.out.println("Integer number: "+a);
        System.out.println("Float number: "+b);
        System.out.println("Double number: "+c);
        System.out.println("Long number: "+e);
        System.out.println("Character: "+f);
        System.out.println("Boolean: "+g);
        System.out.println("String value: "+str);
    }
}